myArray = [3, 4, 1, 2, 5]

print("Given Array:",myArray)

#Sort the Array
sortedArray = sorted(myArray)
print("Sorted Array:",sortedArray)

#Minimum Value : 0th index in Array
min_val = sortedArray[0]

#Maximum Value : last index in Array
max_val = sortedArray[4]

print("Minimum Value:",min_val)
print("Maximum Value:",max_val)

